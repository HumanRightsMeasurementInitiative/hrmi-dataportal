const locales = ['en', 'es', 'pt', 'fr', 'zh'];

const createSitemap = async () => {
  fs.writeFileSync(sitemapFile, '<?xml version="1.0" encoding="UTF-8"?>\n');
  fs.appendFileSync(
    sitemapFile,
    '<urlset xmlns="http://www.sitemaps.org/schemes/sitemap/0.9">\n',
  );

  for (const locale of locales) {
    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}</loc>\n  </url>\n`,
    );

    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}/about</loc>\n  </url>\n`,
    );

    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}/methodology</loc>\n  </url>\n`,
    );

    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}/countries</loc>\n  </url>\n`,
    );

    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}/metrics</loc>\n  </url>\n`,
    );

    fs.appendFileSync(
      sitemapFile,
      `  <url>\n    <loc>https://rightstracker.org/${locale}/groups</loc>\n  </url>\n`,
    );

    for (const country of countries) {
      const line = `  <url>\n    <loc>https://rightstracker.org/${locale}/country/${country}</loc>\n  </url>\n`;
      fs.appendFileSync(sitemapFile, line);
    }

    for (const right of rights) {
      const line = `  <url>\n    <loc>https://rightstracker.org/${locale}/metric/${right}</loc>\n  </url>\n`;
      fs.appendFileSync(sitemapFile, line);
    }

    for (const group of Array(39).keys()) {
      const line = `  <url>\n    <loc>https://rightstracker.org/${locale}/groups/${group +
        1}</loc>\n  </url>\n`;
      fs.appendFileSync(sitemapFile, line);
    }
  }

  fs.appendFileSync(sitemapFile, '</urlset>\n');
};

createSitemap();
